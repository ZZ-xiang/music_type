int lcdfd = 0;
int* ld = NULL;
//建立映射
int initlcd()
{
    lcdfd = open("/dev/fb0",O_RDWR);
	
    if (lcdfd == -1) {
        perror("Open error");
		return -1;
    }
    ld = mmap(NULL,480*800*32/8,PROT_READ | PROT_WRITE,MAP_SHARED,lcdfd,0);
    
    if (ld == MAP_FAILED )
    {
		perror("mmap faild");
		return -1;
	}
	return 0;
}

//解除映射
void uninitlcd()
{
	munmap(ld,480*800*32/8);
	close(lcdfd);
} 

/*
**画点函数
*/
void DrawPoint(int x,int y,int color)
{
	*(ld+y*800+x) = color;
}  

void lcd_draw_word(int x0,int y0,char ch[], int len, int w, int color)
{
	int i, j;
	int flag;
	//flag表示一行有多少个字节
	flag = w/8;
	//遍历整个字模数组
	for (i = 0; i < len; i++)
	{
		//求每一个字节中的每一位
		for (j = 7; j >= 0; j--)
		{
			if ((ch[i] >> j) & 1)
			{
				//画一个点
				DrawPoint(7-j + 8*(i%flag) + x0,i/flag + y0,color);
			}
		}
	}
}

/*
*	清屏函数
**/
void ClearPoint(int color)
{
	for(int i=0;i<480;i++)
	{
		for(int j=0;j<800;j++)
		{
			DrawPoint(j,i,color);
		}
	}
}

